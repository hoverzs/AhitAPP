const LOG_PREFIX = "[generation-job]";

function jobLog(message: string, meta?: Record<string, unknown>): void {
  console.log(
    `${LOG_PREFIX} ${new Date().toISOString()} — ${message}`,
    meta ?? ""
  );
}

function jobWarn(message: string, meta?: Record<string, unknown>): void {
  console.warn(
    `${LOG_PREFIX} ${new Date().toISOString()} — ${message}`,
    meta ?? ""
  );
}

function jobError(message: string, meta?: Record<string, unknown>): void {
  console.error(
    `${LOG_PREFIX} ${new Date().toISOString()} — ${message}`,
    meta ?? ""
  );
}

export function isRetriableCronGenerationError(error: unknown): boolean {
  if (error instanceof GenerationSkippedError) return false;

  if (isGeminiOverloadError(error)) return true;

  const details = toGeminiErrorDetails(error);
  return (
    details.code === "GEMINI_OVERLOAD" ||
    details.code === "NETWORK" ||
    details.code === "TIMEOUT" ||
    details.code === "QUOTA" ||
    details.code === "MAX_TOKENS" ||
    details.code === "API_HTTP" ||
    details.code === "EMPTY_RESPONSE" ||
    details.code === "DUPLICATE_VERSE" ||
    details.code === "UNKNOWN"
  );
}

function resolveAttemptPhase(job: DailyGenerationJob): GenerationAttemptLogEntry["phase"] {
  if (job.retry_count === 0) return "initial";
  return job.phase;
}

function appendLog(
  job: DailyGenerationJob,
  entry: Omit<GenerationAttemptLogEntry, "attemptNumber">
): void {
  const attemptNumber = job.retry_count + 1;
  job.logs.push({ ...entry, attemptNumber });
  job.retry_count = attemptNumber;
}

async function markJobPublished(
  job: DailyGenerationJob,
  publishedAt: string
): Promise<DailyGenerationJob> {
  job.status = "published";
  job.published_at = publishedAt;
  job.next_retry_at = null;
  job.last_error = null;
  job.last_error_code = null;
  await saveGenerationJob(job);
  return job;
}

/** Idempotens: ha már van áhítat a napra, sikernek tekintjük. */
export async function ensureDevotionalNotAlreadyPublished(
  date: string,
  job: DailyGenerationJob | null
): Promise<{ skipped: true; job: DailyGenerationJob | null } | { skipped: false }> {
  const history = await readDevotionals();
  const existing = getDevotionalByDate(history, date);

  if (!existing) {
    return { skipped: false };
  }

  jobLog("Már létezik áhítat — duplikáció elkerülve", {
    date,
    dayNumber: existing.dayNumber,
    title: existing.title,
  });

  if (job) {
    appendLog(job, {
      at: new Date().toISOString(),
      phase: resolveAttemptPhase(job),
      success: true,
      message: `Már publikálva: ${existing.dayNumber}. nap`,
    });
    await markJobPublished(
      job,
      existing.generatedAt ?? existing.updatedAt ?? new Date().toISOString()
    );
    return { skipped: true, job };
  }

  return { skipped: true, job: null };
}

export interface GenerationAttemptResult {
  success: boolean;
  skipped?: boolean;
  job: DailyGenerationJob;
  devotional?: Awaited<ReturnType<typeof autoGenerateDailyDevotional>>["devotional"];
  error?: string;
  code?: string;
}

/**
 * Egy generálási kísérlet — naplózás + job állapot frissítés.
 */
export async function runGenerationAttempt(
  date: string,
  options?: { trigger?: "cron" | "retry_processor" | "manual" }
): Promise<GenerationAttemptResult> {
  const trigger = options?.trigger ?? "cron";
  const job = (await getGenerationJob(date)) ?? createEmptyGenerationJob(date);

  const idempotent = await ensureDevotionalNotAlreadyPublished(date, job);
  if (idempotent.skipped) {
    return {
      success: true,
      skipped: true,
      job: idempotent.job ?? job,
    };
  }

  if (job.status === "failed") {
    jobWarn("Feladat FAILED állapotban — kihagyva", { date, trigger });
    return { success: false, job, error: job.last_error ?? "Sikertelen generálás." };
  }

  if (job.status === "published") {
    return { success: true, skipped: true, job };
  }

  job.status = "running";
  job.last_attempt_at = new Date().toISOString();
  await saveGenerationJob(job);

  const phase = resolveAttemptPhase(job);
  jobLog(`Generálás indult (${trigger}, ${phase})`, {
    date,
    retry_count: job.retry_count,
    next_retry_at: job.next_retry_at,
  });

  try {
    const result = await autoGenerateDailyDevotional(date, { forcePublished: true });
    const publishedAt = new Date().toISOString();

    appendLog(job, {
      at: publishedAt,
      phase,
      success: true,
      message: `Sikeres: ${result.devotional.dayNumber}. nap — „${result.devotional.title}”`,
    });

    await markJobPublished(job, publishedAt);

    jobLog("Sikeres generálás és közzététel", {
      date,
      dayNumber: result.devotional.dayNumber,
      attempt: job.retry_count,
    });

    return {
      success: true,
      job,
      devotional: result.devotional,
    };
  } catch (error) {
    if (error instanceof GenerationSkippedError) {
      const history = await readDevotionals();
      const existing = getDevotionalByDate(history, date);
      if (existing) {
        appendLog(job, {
          at: new Date().toISOString(),
          phase,
          success: true,
          message: error.reason,
        });
        await markJobPublished(
      job,
      existing.generatedAt ?? existing.updatedAt ?? new Date().toISOString()
    );
        return { success: true, skipped: true, job, devotional: existing };
      }
    }

    const details = toGeminiErrorDetails(error);

    appendLog(job, {
      at: new Date().toISOString(),
      phase,
      success: false,
      error: details.message,
      code: details.code,
    });

    job.last_error = details.message;
    job.last_error_code = details.code;
    job.last_attempt_at = new Date().toISOString();

    logGeminiError(error, `${LOG_PREFIX} ${date}`);
    jobError("Generálási kísérlet sikertelen", {
      date,
      attempt: job.retry_count,
      ...describeErrorForLog(error),
    });

    if (!isRetriableCronGenerationError(error)) {
      job.status = "failed";
      job.next_retry_at = null;
      await saveGenerationJob(job);
      jobError("Nem újrapróbálható hiba — FAILED", {
        date,
        code: details.code,
      });
      return { success: false, job, error: details.message, code: details.code };
    }

    const nextAt = scheduleNextRetry(job);
    if (!nextAt) {
      job.status = "failed";
      job.next_retry_at = null;
      await saveGenerationJob(job);
      jobError("Összes retry kimerült — FAILED", { date, retry_count: job.retry_count });
      return { success: false, job, error: details.message, code: details.code };
    }

    job.status = "pending_retry";
    job.next_retry_at = nextAt.toISOString();
    await saveGenerationJob(job);

    jobWarn("PENDING_RETRY — következő próba ütemezve", {
      date,
      retry_count: job.retry_count,
      next_retry_at: job.next_retry_at,
      phase: job.phase,
    });

    return { success: false, job, error: details.message, code: details.code };
  }
}

/** Esedékes retry feladat feldolgozása (mai nap). */
export async function processDueGenerationRetries(
  now = new Date()
): Promise<GenerationAttemptResult | { processed: false; reason: string }> {
  const date = getAppTodayIso(now);
  const job = await getGenerationJob(date);

  if (!job) {
    return { processed: false, reason: "Nincs aktív feladat a mai napra." };
  }

  if (job.status === "published" || job.status === "failed") {
    return { processed: false, reason: `Feladat állapota: ${job.status}` };
  }

  if (job.status !== "pending_retry") {
    return { processed: false, reason: "Nincs függőben lévő retry." };
  }

  if (!job.next_retry_at) {
    return { processed: false, reason: "Hiányzó next_retry_at." };
  }

  if (new Date(job.next_retry_at).getTime() > now.getTime()) {
    return {
      processed: false,
      reason: `Következő retry: ${job.next_retry_at}`,
    };
  }

  jobLog("Ütemezett retry indítása", {
    date,
    retry_count: job.retry_count,
    next_retry_at: job.next_retry_at,
  });

  const result = await runGenerationAttempt(date, { trigger: "retry_processor" });
  return result;
}
